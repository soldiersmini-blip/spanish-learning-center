function category(
  categoryName: string,
  base: Term[],
  nouns: NounTerm[],
  verbs: VerbTerm[],
  objects: Term[],
  target: number,
): VocabularyItem[] {
  const items = [
    ...simple(L, categoryName, base, exampleForTerm),
    ...adjectivePhrases(L, categoryName, nouns, a2Adjectives, Math.max(0, target - base.length) / 2),
    ...verbObjectPhrases(L, categoryName, verbs, objects, target),
  ];
  return items.slice(0, target);
}

