const L = (zh: string, en: string, es: string) => ({ zh, en, es });
const v = (spanish: string, zh: string, en: string, example: string): VocabItem => ({ spanish, zh, en, example });
const c = (zh: string, en: string, es: string, items: VocabItem[]): VocabCategory => ({ title: L(zh, en, es), items });
const g = (zh: string, en: string, es: string, explanationZh: string, examples: string[]): GrammarPoint => ({
  title: L(zh, en, es),
  explanation: L(explanationZh, en, es),
  examples,
});
const s = (themeZh: string, themeEn: string, themeEs: string, spanish: string, zh: string, en: string, slot: string): SentencePattern => ({
  theme: L(themeZh, themeEn, themeEs),
  spanish,
  zh,
  en,
  slot,
});
const skill = (zh: string, en: string, es: string, contentZh: string, examples: string[]): SkillItem => ({
  title: L(zh, en, es),
  content: L(contentZh, en, es),
  examples,
});

